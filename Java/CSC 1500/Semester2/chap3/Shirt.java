class Shirt {

   public static void shirts(int col, int sle) {

      int collarsize, sleevelength;
      String material = "cotton";

      collarsize = col;
      sleevelength = sle;

      System.out.println();
      System.out.println("Collar size = " + collarsize);
      System.out.println("Sleeve length = " + sleevelength);
      System.out.println("Material = " + material);
 
   } // method shirts

} // class Shirt
