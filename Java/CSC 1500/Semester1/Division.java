// Demonstrating the division operators. 

class Division {

	public static void main (String[] args) {

		int oper1 = 9, oper2 = 4;
		double oper3 = 4.0;

		System.out.println ("Integer division: " + oper1/oper2);
		System.out.println ("Floating division: " + oper1/oper3);
		System.out.println ("Remainder division: " + oper1%oper2);

	} // method main

} // class Division

